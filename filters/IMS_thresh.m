function IMS_thr = IMS_thresh(IMS_new,pctile)
% -------------------------------------------------------------------------
% Removes very bright pixels and inverts the image
% -------------------------------------------------------------------------
    thresh =  prctile(IMS_new(:),pctile);
    IMS_new(IMS_new > thresh) = thresh;

    IMS_thr = max(IMS_new(:)) - IMS_new;
end
